/* ---- Active theme ----------------------------------------------------- */

bs_theme_t g_bs_theme;

void bs_theme_set(const bs_theme_t* theme) {
    g_bs_theme = *theme;
}

void bs_theme_apply(int palette_idx, bs_border_style_t border_style) {
    if (palette_idx < 0 || palette_idx >= BS_PALETTE_COUNT) palette_idx = 0;
    const bs_palette_t* p = bs_palettes[palette_idx];
    g_bs_theme.bg        = p->bg;
    g_bs_theme.primary   = p->primary;
    g_bs_theme.secondary = p->secondary;
    g_bs_theme.dim       = p->dim;
    g_bs_theme.accent    = p->accent;
    g_bs_theme.warn      = p->warn;
    g_bs_theme.border    = border_style;
}
